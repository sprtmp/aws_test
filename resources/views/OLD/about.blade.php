                        <p class="text-left">The website is being handled by Lepidoptera Section, ZSI, Kolkata, which is primarily involved in collection, preservation, identification, cataloguing, computerization, digitization and documentation of Lepidopteran (moths and butterflies) fauna of our country. The section holds more than 1, 15, 000 specimens of Indian Lepidoptera. There are about 4000 species and subspecies of butterflies (including from outside India) and more than 2500 species and subspecies of moths housed in the section. The Lepidoptera section houses collection from 49 countries and more than 750 type specimens. The first registered specimen of Lepidoptera section is a male butterfly, Hasora umbrina Mab., collected by N. E. Celebes and was catalogued by Cap. Evans on October 01, 1931. The collections of de. Niceville, K. Jordan, W.H. Evans, E. E. Green, G. C. Dudgeon are some of the historical collections housed in the Lepidoptera section.</p>
